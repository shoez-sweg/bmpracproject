from scipy.stats import spearmanr

# Define enzymes to compare
enzymes = {
    'LBXSATSI': 'ALT (U/L)',
    'LBXSASSI': 'AST (U/L)',
    'LBXSGTSI': 'GGT (U/L)'
}

# Interpret correlation strength
def interpret_correlation(rho):
    abs_rho = abs(rho)
    if abs_rho < 0.1:
        return "negligible"
    elif abs_rho < 0.3:
        return "weak"
    elif abs_rho < 0.5:
        return "moderate"
    else:
        return "strong"

# Run and interpret test
print("\nüìä Spearman Correlation between Sleep Duration and Liver Enzymes:\n")
for enz, label in enzymes.items():
    if enz in df_cleaned.columns:
        sleep = df_cleaned['SLD010H']
        biomarker = df_cleaned[enz]
        mask = ~sleep.isna() & ~biomarker.isna()

        rho, p = spearmanr(sleep[mask], biomarker[mask])

        # Direction
        direction = "positive" if rho > 0 else "negative"

        # Strength interpretation
        strength = interpret_correlation(rho)

        # Significance interpretation
        significance = "statistically significant" if p < 0.05 else "not statistically significant"

        # Final summary
        print(f" - {label}:")
        print(f"   Spearman œÅ = {rho:.3f}, p = {p:.4f}")
        print(f"   ‚û§ There is a {strength}, {direction} correlation between sleep duration and {label}, which is {significance}.\n")

